edad = int(input("¿Cuántos años tienes? "))
if edad < 18:
    print("Aun te quedan muchas cosas por vivir y aprender")
elif 18 <= edad < 33:
    print("Estás en la flor de la vida, aprovecha cada momento y disfruta")
elif edad >= 33:
    print("Si estuviésemos en Lumière, ya habrias fallecido...\npor suerte para ti no es el caso ;)")
